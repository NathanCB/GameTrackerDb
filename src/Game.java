
public class Game {
    int id;
    String gameName;
    String gameGenre;
    String gamePlatform;
    int gameYear;

    public Game(int id, String gameName, String gameGenre, String gamePlatform, int gameYear) {
        this.id = id;
        this.gameName = gameName;
        this.gameGenre = gameGenre;
        this.gamePlatform = gamePlatform;
        this.gameYear = gameYear;
    }
}
